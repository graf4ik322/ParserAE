    echo ""
    echo "üõë –û—Å—Ç–∞–Ω–æ–≤–∫–∞:"
    echo "   docker-compose down"
    echo ""
    echo "–ù–∞–∂–º–∏—Ç–µ Ctrl+C –¥–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ –∏–ª–∏ –∑–∞–ø—É—Å—Ç–∏—Ç–µ –≤ —Ñ–æ–Ω–µ:"
